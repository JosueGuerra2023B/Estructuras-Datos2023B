#Operadores racionales

print(" -*- Operadores racionales -*- ")
x = 9
y = 1

print("x = ", x)
print("y = ", y)

menor_que = x < y
mayor_que = x > y
igual =  x == y
no_igual = x != y
menor_que_o_igual = x <= y
mayor_que_o_igual = x >= y

print(" Menor que: x < y = ", menor_que)
print(" Mayor que: x > y = ", mayor_que)
print(" Igual: x == y = ", igual)
print(" No igual: x != y = ", no_igual)
print(" Menor que o igual : x <= y = ", menor_que_o_igual)
print(" Mayor que o igual: x >= y = ", mayor_que_o_igual)
